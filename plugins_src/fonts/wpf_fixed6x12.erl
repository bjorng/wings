width_fun() -> fun cw/1.

width() -> 6.
height() -> 12.

draw([C|T]) ->
    char(C),
    draw(T);
draw([]) -> ok.

bold([C|T]) ->
    bold_char(C),
    bold(T);
bold([]) -> ok.

bold_char(C) ->
    char(C),
    Cw = cw(C),
    gl:bitmap(1, 1, 0, 0, -Cw+1, 0, <<0>>),
    char(C).

