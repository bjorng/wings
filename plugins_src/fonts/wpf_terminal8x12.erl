-module(wpf_terminal8x12).
-export([desc/0,width/0,width_fun/0,height/0,draw/1,char/1,bold/1]).

desc() ->
    "Terminal (8x12)".

width_fun() -> fun cw/1.

width() -> 8.
height() -> 12.

draw([C|T]) ->
    char(C),
    draw(T);
draw([]) -> ok.

bold([C|T]) ->
    bold_char(C),
    bold(T);
bold([]) -> ok.

cw(_) -> 8.

bold_char(C) ->
    char(C),
    Cw = cw(C),
    gl:bitmap(1, 1, 0, 0, -Cw+1, 0, <<0>>),
    char(C).

